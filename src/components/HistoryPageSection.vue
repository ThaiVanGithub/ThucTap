                        Năm 1418, sau hơn 10 năm, nghĩa quân Lam Sơn do Lê Lợi lãnh đạo nổi dậy chống lại ách thống trị
                        của nhà Minh. Đến năm 1428, quân Minh hoàn toàn rút khỏi Đại Việt. Lê Lợi lên ngôi hoàng đế, lập
                        ra nhà Hậu Lê, mở đầu thời kỳ trung hưng.
                    </p>
                </div>

                <div class="col-lg-12 col-sm-12">
                    <h3>4.4. Thời kỳ chia cắt (1527 – 1802)</h3>
                    <p>
                        Năm 1527, Mạc Đăng Dung lập nhà Mạc sau khi giành ngôi từ nhà Hậu Lê (nhà Lê trung hưng). Nhà
                        Hậu Lê được tái lập vài năm sau đó với sự giúp đỡ của Nguyễn Kim và giành được kiểm soát khu vực
                        từ Thanh Hóa đến Bình Định. Sau khi Nguyễn Kim qua đời, con rể Trịnh Kiểm tiếp quản quyền bính
                        và gia đình Trịnh đã chiếm ưu thế trước gia đình Mạc vào năm 1592, mở đầu cho thời kỳ vua Lê
                        chúa Trịnh

                        Sự mâu thuẫn giữa hai cận thần của nhà Lê trung hưng, Trịnh Kiểm và Nguyễn Hoàng, đã dẫn đến
                        phân chia đất nước thành hai lãnh thổ: Đàng Ngoài (do gia đình Trịnh cầm quyền) và Đàng Trong
                        (do gia đình Nguyễn cầm quyền). Mở ra thời kỳ Trịnh Nguyễn phân tranh, lấy sông Gianh làm biên
                        giới

                        Trong giai đoạn này, ngoại thương phát triển mạnh , với cả Đàng Ngoài và Đàng Trong tham gia vào
                        hệ thống giao thương toàn cầu với các nước châu Âu, Nhật Bản, Trung Quốc. Các mặt hàng chính
                        được là tơ lụa, hồ tiêu, gốm sứ.

                        Từ giữa thế kỷ XVIII, cuộc sống của người dân càng thêm khó khăn, phong trào Tây Sơn do 3 anh em
                        Nguyễn Nhạc, Nguyễn Lữ và Nguyễn Huệ thành lập đã phát triển lớn mạnh và đánh bại hai chế độ cai
                        trị của gia đình Nguyễn và Trịnh. Tây Sơn đã chiếm hầu hết lãnh thổ và thống nhất đất nước. Tuy
                        nhiên, sau cái chết của vua Nguyễn Huệ, nội bộ Tây Sơn bị xung đột, dẫn đến suy yếu.
                    </p>
                </div>

                <div class="col-lg-12 col-sm-12">
                    <h3>4.5. Thời kỳ thống nhất (1802 – 1858)</h3>
                    <p>
                        Năm 1802 với sự hậu thuẫn của một số người Pháp, Nguyễn Phúc Ánh thuộc dòng dõi chúa Nguyễn đã
                        đánh bại Tây Sơn. Ông lên làm vua lấy niên hiệu là Gia Long và trở thành vị vua đầu tiên của nhà
                        Nguyễn. Ông đổi tên nước từ Đại Việt thành Việt Nam và đóng đô ở Huế.

                        Gia Long và Minh Mạng, đã cố gắng xây dựng Việt Nam theo mô hình Trung Quốc thời nhà Thanh. Tuy
                        nhiên, từ năm 1830, một số giới trí thức Việt Nam đã nhận thấy sự tụt hậu của đất nước và đề
                        xuất học hỏi phương Tây để phát triển công nghiệp – thương mại. Tuy nhiên, các vua sau Gia Long
                        lại coi trọng phát triển nông nghiệp và tiếp tục cấm buôn bán với nước ngoài.

                        Ngoài ra, các vua của nhà Nguyễn lo ngại về sự lớn mạnh của Công Giáo nên ngăn cấm truyền bá,
                        đàn áp những người theo đạo và tiến hành san bằng nhiều xóm đạo.
                    </p>
                </div>

                <div class="col-lg-12 col-sm-12">
                    <h2 class="display-3">5. Thời kỳ hiện đại (1858 – nay)</h2>
                    <h3 id="thoiQuanChu">5.1 Pháp thuộc (1858 – 1945)</h3>
                    <p>
                        Năm 1858, Hải quân Pháp xâm lược Việt Nam bằng việc tấn công Đà Nẵng và và đánh xuống Sài Gòn.
                        Kết quả là Pháp chiếm đóng cả miền Đông và miền Tây, hình thành lãnh thổ thuộc địa Nam Kỳ. Tiếp
                        đó, từ năm 1873 đến năm 1886, Pháp tiếp tục xâm chiếm miền Bắc Việt Nam. Sau đó tiếp tục cho ác
                        vua nhà Nguyễn đến thời Bảo Đại duy trì, tuy nhiên quyền lực hạn chế, tất cả chuyện lớn phải
                        thông qua Toàn quyền Đông Dương của Pháp duyệt.

                        Pháp hoàn tất việc xâm chiếm toàn bộ lãnh thổ Việt Nam và thiết lập chế độ thuộc địa tại Đông
                        Dương. Pháp thành lập bộ máy cai trị khá hoàn chỉnh từ trung ương đến địa phương, chia Việt Nam
                        thành 3 kỳ: Nam Kỳ, Trung Kỳ và Bắc Kỳ, dưới sự quản lý của các quan lại Pháp. Việt Nam bị cải
                        cách giáo dục và thay đổi hệ thống chữ viết từ chữ Hán sang chữ quốc ngữ.

                        Đầu thế kỷ XX, các phong trào dân tộc và cải cách xã hội xuất hiện như phong trào Duy Tân và
                        phong Trào Đông Du nhằm vận động tăng cường dân trí, dân chủ và nhân quyền. Các phong trào này
                        bị thực dân dẹp bỏ vì nguy cơ ảnh hưởng đến chế độ thuộc địa. Việt Nam Quốc dân Đảng (1920) và
                        Đảng Cộng sản Đông Dương (1930) ra đời và cũng nhanh chóng trở thành mục tiêu của Pháp.
                    </p>
                </div>

                <div class="col-lg-12 col-sm-12">
                    <h3>5.2 Thời kỳ cộng hòa (1945 – nay)</h3>
                    <h3>Tuyên bố độc lập</h3>
                    <p>
                        Tháng 8 năm 1945, Việt Minh tổ chức thành công Cách mạng tháng Tám, giành quyền lực ở hầu hết
                        Việt Nam.

                        Ngày 2 tháng 9 năm 1945, hủ tịch Hồ Chí Minh tuyên bố thành lập Việt Nam Dân chủ Cộng hòa độc
                        lập và thống nhất từ Bắc tới Nam. Một cuộc bầu cử toàn quốc đã được tổ chức đầu năm 1946, Quốc
                        kỳ mới là cờ nền đỏ với sao vàng năm cánh, và hiến pháp được thông qua. Nhà nước Việt Nam Dân
                        chủ Cộng hòa trở thành đại diện hợp pháp của nhân dân Việt Nam từ Bắc tới Nam.
                    </p>

                    <h3>Kháng chiến chống Pháp (1946 – 1954)</h3>
                    <p>
                        Năm 1947, Pháp chiếm lấy toàn bộ các đô thị lớn của Việt Nam, tuy nhiên Việt Nam Dân chủ Cộng
                        hòa tiếp tục chiến tranh nhân dân và du kích, làm cho Pháp sa lầy. Năm 1949, Pháp đành đàm phán
                        với các chính trị gia Việt Nam chống lại chính quyền Hồ Chí Minh và thành lập chính phủ mới là
                        Quốc gia Việt Nam do Bảo Đại lãnh đạo, nhưng vẫn dưới sự hỗ trợ của Pháp.

                        Năm 1950, Trung Hoa và Liên Xô trợ giúp Việt Nam Dân chủ Cộng hòa vũ khí, trong khi Mỹ hậu thuẫn
                        Pháp. Thất bại tại trận Điện Biên Phủ năm 1954 khiến Pháp và Mỹ phải rút quân khỏi Việt Nam.

                        Hiệp định Genève được ký kết năm 1954, tạm thời phân chia Việt Nam thành hai miền. Miền Bắc do
                        chính quyền Hồ Chí Minh lãnh đạo, còn miền Nam do Quốc gia Việt Nam do Bảo Đại cầm quyền. Hiệp
                        định quy định tổ chức tuyển cử để thống nhất đất nước, nhưng cuộc tuyển cử không diễn ra do sự
                        can thiệp của Mỹ.
                    </p>

                    <h3>Chiến tranh Việt Nam (1955 – 1975)</h3>
                    <p>
                        Trong giai đoạn 1956 – 1958, Việt Nam Dân chủ Cộng hòa đề nghị Quốc gia Việt Nam tổ chức tuyển
                        cử thống nhất đất nước nhưng bị từ chối. Năm 1955, với sự hỗ trợ của Mỹ, Ngô Đình Diệm gian lận
                        trong Cuộc trưng cầu dân ý miền Nam Việt Nam để trở thành Tổng thống của chính phủ Việt Nam Cộng
                        hòa. Mỹ hỗ trợ Diệm xây dựng cải cách điền địa và củng cố quân đội.

                        Chính phủ Ngô Đình Diệm bắt đầu đàn áp chính trị và tôn giáo, gây ra nhiều cuộc thảm sát và mâu
                        thuẫn tôn giáo sâu sắc. Năm 1959, Mặt trận Dân tộc Giải phóng miền Nam Việt Nam được hình thành
                        để kêu gọi tổ chức tuyển cử thống nhất đất nước.

                        Sau đó, Chiến tranh Việt Nam tiếp tục bùng nổ với sự tham gia của quân đội Mỹ từ năm 1965. Tuy
                        nhiên, các chiến lược của Mỹ không đem lại kết quả như mong đợi., chính thức tham gia chiến
                        tranh. Tình hình trở nên hỗn loạn và nội bộ nền Đệ nhị Cộng hòa bị suy sụp sau nhiều giai đoạn
                        đảo chính. Cuối cùng, sau Hiệp định Paris năm 1973, quân viễn chinh Mỹ rút khỏi Việt Nam, nhưng
                        chiến tranh vẫn tiếp diễn với việc QLVNCH vi phạm hiệp định.

                        Cuối cùng, vào ngày 30 tháng 4 năm 1975, Quân Giải phóng miền Nam Việt Nam chiếm được Sài Gòn,
                        chính phủ Đệ nhị Cộng hòa tuyên bố đầu hàng.
                    </p>

                    <h3>Thời kỳ đầu sau thống nhất (1976 – 1986)</h3>
                    <p>
                        Từ năm 1976 đến 1986, sau khi thống nhất, Việt Nam đã đối mặt với nhiều khó khăn và khủng hoảng
                        kinh tế – xã hội. Việt Nam Dân chủ Cộng hòa và Cộng hòa miền Nam Việt Nam tổ chức Tổng tuyển cử
                        thống nhất quốc gia, đổi tên thành Cộng hòa Xã hội chủ nghĩa Việt Nam.

                        Nền kinh tế đối mặt với khủng hoảng và tỷ lệ lạm phát tăng cao. Xung đột biên giới với Trung
                        Quốc và chiến tranh biên giới Việt – Trung diễn ra, gây nên những hậu quả nghiêm trọng. Việt Nam
                        cũng phải đối mặt với xâm phạm và cuộc tấn công của quân đội Khmer Đỏ từ Campuchia. Tuy nhiên,
                        dưới sự lãnh đạo của chính phủ, Việt Nam đã tiến hành can thiệp và chấm dứt chế độ Khmer Đỏ tại
                        Campuchia, mặc dù phải đối mặt với cuộc tấn công của Trung Quốc đang ủng hộ chế độ Khmer Đỏ.
                        Toàn bộ những thử thách này đã đặt ra sức ép lớn đối với chính trị và quản lý kinh tế của Việt
                        Nam.

                        Trong thập niên 1980, tình hình khủng hoảng kinh tế và các xung đột biên giới đã khiến Việt Nam
                        đối mặt với cuộc khủng hoảng “Hoa kiều”, khi nhiều người gốc Hoa chạy khỏi nước. Cũng trong thời
                        gian này, Trung Quốc cũng tấn công và chiếm đóng một số bãi đá và quần đảo của Việt Nam trên
                        Biển Đông.
                    </p>

                    <h3>Thời kỳ đổi mới (1986 – nay)</h3>
                    <p>
                        Từ năm 1986 khi Đại hội Đảng Cộng sản Việt Nam VI triển khai chính sách “Đổi Mới” dưới sự lãnh
                        đạo của ông Nguyễn Văn Linh. Chính sách này nhằm cải cách cơ cấu Đảng, chính quyền, và kinh tế
                        theo hướng thị trường với định hướng Xã hội chủ nghĩa.

                        Công cuộc đổi mới đã mang lại những kết quả tích cực cho nền kinh tế Việt Nam. Kinh tế được cải
                        thiện đáng kể, từ một quốc gia nhập khẩu và nhận viện trợ từ nước ngoài, Việt Nam trở thành quốc
                        gia xuất khẩu. Xuất khẩu gạo của Việt Nam tăng mạnh và trở thành một trong những nước khẩu gạo
                        lớn nhất thế giới. Lạm phát giảm dần và kinh tế đạt tốc độ tăng trưởng cao.

                        Việt Nam đã thiết lập quan hệ ngoại giao với nhiều nước và khu vực, tham gia các tổ chức kinh tế
                        quốc tế như WTO và APEC. Tuy nhiên, Việt Nam cũng phải đối mặt với những thách thức như cải cách
                        thể chế, và tái cơ cấu kinh tế để nâng cao năng lực cạnh tranh trong khu vực và trên thế giới.

                        Ngày 31/12/2015, Việt Nam tham gia thành lập Cộng đồng Kinh tế ASEAN – ASEAN Economic Community
                        (AEC), bao gồm 10 quốc gia thành viên. Tham gia AEC mang đến cơ hội quý báu để Việt Nam tăng
                        cường xuất khẩu, thu hút đầu tư nước ngoài để bắt kịp với xu thế và trình độ phát triển kinh tế
                        của khu vực và thế giới.

                        Như vậy, qua hàng nghìn năm lịch sử, Việt Nam luôn giữ vững tinh thần độc lập dân tộc. Lịch sử
                        Việt Nam đã chứng minh ý chí kiên cường bất khuất của dân tộc trong sự nghiệp xây dựng và bảo vệ
                        đất nước.
                    </p>

                </div>

